""" –†–∞–±–æ—Ç–∞ —Å –º–Ω–æ–∂–µ—Å—Ç–≤–∞–º–∏ """
""" –ú–µ—Ç–æ–¥ intersection() - –ü–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤, &

–î–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏—è –º–Ω–æ–∂–µ—Å—Ç–≤, —Ç.–µ. –º–Ω–æ–∂–µ—Å—Ç–≤–∞ —Å —ç–ª–µ–º–µ–Ω—Ç–∞–º–∏, –∫–æ—Ç–æ—Ä—ã–µ –µ—Å—Ç—å –∏
–≤ –ª–µ–≤–æ–º –∏ –≤ –ø—Ä–∞–≤–∞–º –º–Ω–æ–∂–µ—Å—Ç–≤–µ –∏—Å–ø–æ–ª—å–∑—É—é—Ç –º–µ—Ç–æ–¥ intersection"""

my_set = {3, 4, 2, 5, 6, 1, 7}
other_set = {1, 4, 42, 314}
new_set = my_set.intersection(other_set)
print(f'{my_set = }\n{other_set = }\n{new_set = }')     # my_set = {1, 2, 3, 4, 5, 6, 7}
                                                        # other_set = {1, 42, 4, 314}
                                                        # new_set = {1, 4}

""" –ù–æ–≤–∞—è –≤–µ—Ä—Å–∏—è Python –ø–æ–∑–≤–æ–ª—è–µ—Ç –ø–æ–ª—É—á–∏—Ç—å –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ –º–Ω–æ–∂–µ—Å—Ç–≤ –≤ —Å–ª–µ–¥—É—é—â–µ–π
–∑–∞–ø–∏—Å–∏ c –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Å–∏–º–≤–æ–ª–∞ & """

my_set = {3, 4, 2, 5, 6, 1, 7}
other_set = {1, 4, 42, 314}
new_set = my_set & other_set
print(f'{my_set = }\n{other_set = }\n{new_set = }')     # my_set = {1, 2, 3, 4, 5, 6, 7}
                                                        # other_set = {1, 42, 4, 314}
                                                        # new_set = {1, 4}

